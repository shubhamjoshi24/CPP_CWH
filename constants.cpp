#include <bits/stdc++.h>
using namespace std;
int main()
{
    /*  Constant are unchangeable ;when a constant variable is initialized in a program; it's value can't be changed afterwards.*/
    const int a = 6;
    cout << "The value of a is :" << a << endl;
    a = 435;
    cout << a << endl;
    return 0;
}